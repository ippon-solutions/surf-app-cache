package uk.co.ipponsolutions.surfapp.domain;

public class UriDetails {

	private String weatherUri;
	private String waveUri;
	private String tidalUri;
	
	public String getWeatherUri() {
		return weatherUri;
	}
	public void setWeatherUri(String weatherUri) {
		this.weatherUri = weatherUri;
	}
	public String getWaveUri() {
		return waveUri;
	}
	public void setWaveUri(String waveUri) {
		this.waveUri = waveUri;
	}
	public String getTidalUri() {
		return tidalUri;
	}
	public void setTidalUri(String tidalUri) {
		this.tidalUri = tidalUri;
	}
	
}
